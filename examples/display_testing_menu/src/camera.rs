//! GC2145 camera support for the SkyRizz E32.
//!
//! Pin map (FPC3 / DVP, shared SCCB on the main I2C bus):
//!   XCLK = GPIO7  (CAM_CLK from LCD_CAM peripheral master clock)
//!   VSYNC = GPIO4
//!   HREF  = GPIO5
//!   PCLK  = GPIO17
//!   D2 -> CAM_DATA0 = GPIO8
//!   D3 -> CAM_DATA1 = GPIO10
//!   D4 -> CAM_DATA2 = GPIO11
//!   D5 -> CAM_DATA3 = GPIO9
//!   D6 -> CAM_DATA4 = GPIO18
//!   D7 -> CAM_DATA5 = GPIO16
//!   D8 -> CAM_DATA6 = GPIO15
//!   D9 -> CAM_DATA7 = GPIO6
//! Reset (C_RST) is gated by the XL9535 expander on P02 (active LOW).
//!
//! GC2145 quirks:
//! - SCCB only ACKs once XCLK is running.
//! - Register 0xFE is the "page select" (0x00 = sensor, 0x01 = AEC,
//!   0x02 = ISP, 0xF0 = system). Always set the page before touching
//!   anything else.
//! - Chip ID is 0x21 / 0x45 at registers 0xF0 / 0xF1 of the system page.
//!
//! Output: RGB565 320x240 (default crop window in the public init table).

use embedded_hal::delay::DelayNs;
use esp_hal::{
    delay::Delay,
    i2c::master::I2c,
    Blocking,
};

/// GC2145 SCCB I2C address (7-bit).
pub const GC2145_ADDR: u8 = 0x3C;

/// Width / height of the RGB565 frame produced by GC2145 with the default
/// settings used here.
pub const FRAME_W: u16 = 320;
pub const FRAME_H: u16 = 240;
pub const FRAME_BYTES: usize = (FRAME_W as usize) * (FRAME_H as usize) * 2;

/// Sentinel for table delays: write `(REG_DLY, ms)`.
const REG_DLY: u8 = 0xFD;
/// End-of-table sentinel (must not collide with any real reg we write).
const REG_END_KEY: u8 = 0xEE;
const REG_END_VAL: u8 = 0xEE;

/// Result of a sensor probe.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Probe {
    /// chip_id_high (reg 0xF0) and chip_id_low (0xF1) in the system page.
    Found { id_h: u8, id_l: u8 },
    NotFound,
    BusError,
}

/// Probe GC2145 on the SCCB. Expects XCLK to already be running.
pub fn probe(i2c: &mut I2c<'_, Blocking>) -> Probe {
    // Address-only probe.
    let mut tmp = [0u8; 1];
    if i2c.read(GC2145_ADDR, &mut tmp).is_err() {
        return Probe::NotFound;
    }
    // Switch to system page and read chip id.
    if i2c.write(GC2145_ADDR, &[0xFE, 0xF0]).is_err() {
        return Probe::BusError;
    }
    let mut h = [0u8; 1];
    let mut l = [0u8; 1];
    if i2c.write_read(GC2145_ADDR, &[0xF0], &mut h).is_err() {
        return Probe::BusError;
    }
    if i2c.write_read(GC2145_ADDR, &[0xF1], &mut l).is_err() {
        return Probe::BusError;
    }
    Probe::Found { id_h: h[0], id_l: l[0] }
}

/// Apply the default GC2145 init then switch the output to RGB565 320x240.
pub fn init_gc2145_rgb565(i2c: &mut I2c<'_, Blocking>) -> Result<(), ()> {
    let mut delay = Delay::new();
    apply_table(i2c, &mut delay, GC2145_DEFAULT_INIT)?;
    apply_table(i2c, &mut delay, GC2145_RGB565_OVERRIDE)?;
    Ok(())
}

fn apply_table(
    i2c: &mut I2c<'_, Blocking>,
    delay: &mut Delay,
    table: &[(u8, u8)],
) -> Result<(), ()> {
    for &(reg, val) in table {
        if reg == REG_END_KEY && val == REG_END_VAL {
            return Ok(());
        }
        if reg == REG_DLY {
            delay.delay_ms(val as u32);
            continue;
        }
        if i2c.write(GC2145_ADDR, &[reg, val]).is_err() {
            return Err(());
        }
    }
    Ok(())
}

